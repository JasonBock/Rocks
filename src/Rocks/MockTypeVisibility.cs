namespace Rocks;

/// <summary>
/// Allows a developer to specify the visibility
/// of the generated mock type.
/// </summary>
public enum MockTypeVisibility
{
	/// <summary>
	/// Specifies the visibility as <c>private</c>
	/// </summary>
	Private,
	/// <summary>
	/// Specifies the visibility as <c>public</c>
	/// </summary>
	Public,
	/// <summary>
	/// Specifies the visibility as <c>internal</c>
	/// </summary>
	Internal
}